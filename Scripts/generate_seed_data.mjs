const TARGET_COUNT = 10_000;

const range = (count, mapper) => Array.from({ length: count }, (_, index) => mapper(index));

const unique = (values) => [...new Set(values.map((value) => value.trim()).filter(Boolean))];

const addCombinations = (target, left, right) => {
  for (const a of left) {
    for (const b of right) {
      target.push(`${a}${b}`);
    }
  }
};

const writeJson = (path, values) => {
  const data = unique(values);
  const withNo = data.filter((value) => value.includes("の"));
  if (withNo.length > 0) {
    throw new Error(`${path}: 「の」を含む項目があります: ${withNo.slice(0, 5).join(", ")}`);
  }
  if (data.length < TARGET_COUNT) {
    throw new Error(`${path}: ${TARGET_COUNT}件に足りません (${data.length}件)`);
  }
  writeFileSync(path, JSON.stringify(data.slice(0, TARGET_COUNT), null, 2) + "\n");
};

